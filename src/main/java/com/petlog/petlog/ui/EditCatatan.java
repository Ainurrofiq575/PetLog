    private void bthSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bthSimpanActionPerformed
        // TODO add your handling code here:
        String jenisPerawatan = txtJenisPerawatan.getText();
        String tanggal = txtTanggal.getText();
        String catatan = txtCatatanTambahan.getText();

        if (jenisPerawatan.isEmpty() || tanggal.isEmpty()) {
            JOptionPane.showMessageDialog(this, "‚ö†Ô∏è Isi semua data penting!");
            return;
        }

        CatatanService.updateCatatan(id, jenisPerawatan, tanggal, catatan);
        JOptionPane.showMessageDialog(this, "‚úÖ Catatan berhasil diperbarui.");

        this.dispose(); // Tutup dialog
    }//GEN-LAST:event_bthSimpanActionPerformed

