    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
// Ambil input dari form
        String namaHewan = txtNamaHewan.getText();
        String namaVaksin = txtNamaVaksin.getText();
        String tanggal = txtTanggal.getText(); // Format: yyyy-MM-dd

// Validasi input
        if (namaHewan.isEmpty() || namaVaksin.isEmpty() || tanggal.isEmpty()) {
            JOptionPane.showMessageDialog(this, "‚ö†Ô∏è Isi semua field terlebih dahulu!");
            return;
        }

// Simpan ke MongoDB
        VaksinService.simpan(namaHewan, namaVaksin, tanggal);
        JOptionPane.showMessageDialog(this, "‚úÖ Jadwal vaksin berhasil disimpan!");

// Bersihkan input form
        txtNamaHewan.setText("");
        txtNamaVaksin.setText("");
        txtTanggal.setText("");

    }//GEN-LAST:event_btnSimpanActionPerformed

